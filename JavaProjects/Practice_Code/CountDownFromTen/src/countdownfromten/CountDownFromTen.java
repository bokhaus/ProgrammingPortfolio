/*
 * Count down from 10 Demo FOR loop
 * You can change the final value based upon the
 * >= or > or < or <= AND the increment of -- or ++
 * for(int i=1; i<10; i++) OR for(int i=1; i<=10; i++)
 * for(int i=10; i>=1; i--) or for(int i=10; i>1; i--)
 * 
 */
package countdownfromten;


public class CountDownFromTen {

    
    public static void main(String[] args) {
       for(int i=10; i>1; i--){
              System.out.println("The value of i is: "+i);
         }
    }
}
