# Tuples (Type of data structure) similar to list
# Used when data does not need to be changed

# Tuples are immutable (elements cannot be changed)
coordinates = (4, 5)
# List of tuples
coordinates2 = [(4, 5), (10, 20), (35, 12)]

print()
print("The current tuple is...")
print(coordinates)
print()
print("Printing index position 0")
print(coordinates[0])
print("Printing index position 1")
print(coordinates[1])
print()
print("Printing list of tuples")
print(coordinates2)










